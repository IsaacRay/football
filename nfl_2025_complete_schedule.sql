-- Update function to mark current week based on date
CREATE OR REPLACE FUNCTION update_current_week()
RETURNS void AS $$
DECLARE
  current_week_num INTEGER;
BEGIN
  SELECT week_number INTO current_week_num
  FROM games 
  WHERE season = 2025 
    AND game_time <= NOW() + INTERVAL '7 days'
    AND is_complete = false
  ORDER BY game_time
  LIMIT 1;
  
  IF current_week_num IS NOT NULL THEN
    UPDATE pools 
    SET current_week = current_week_num,
        updated_at = NOW()
    WHERE season = 2025 OR season IS NULL;
  END IF;
END;
$$ LANGUAGE plpgsql;

